# a, Python không cho phép các ký tự @, $, %, #, ...xuất hiện trong tên biến.
# Các ký tự có thể được sử dụng để đặt tên cho biến trong python là a đến z, A đến Z, 0 đến 9, gạch dưới _, chữ hán tự, tiếng việt có dấu v.v.

# b, Tên biến hợp lệ: name, age,...
# Tên biến không hợp lệ: 1a, @pple,...

# c, Qua đoạn văn, có thể hiểu là tác giả muốn hướng chúng ta tới quy tắc đặt tên mà bất kì ai khi đọc code cũng hiểu được ý nghĩa và vai trò của biến hay hàm đó trong đoạn code. Điều này sẽ khiến công việc dễ dàng và trơn tru hơn khi mọi người trong team có thể hiểu được các đoạn code của nhau.

# d, Các phong cách đặt tên biến lần lượt là:
# ● viết liền thường hết
# ● viết liền in hoa hết
# ● camelCase
# ● PascalCase
# ● snake_case

# e, PascalCase (Upper Camel Case): viết yêu cầu viết hoa chữ cái đầu tiên của mọi từ. Example: Apple, StudentId, ...
# camelCase (Lower Camel Case): viết hoa chữ cái đầu tiên của mọi từ, ngoại trừ chữ cái đầu tiên của cụm từ. Example: apple,studentId, ...
# underscore: tất cả các từ đều viết thường và dùng dấu gạch dưới ngăn cách giữa các từ. Example: apple, student_id,...

# f, Bình thường thì em sẽ chọn phong cách underscore vì theo em nó sẽ dễ nhìn và đọc lướt cũng sẽ nhanh hơn hai phong cách còn lại tuy nhiên nhược điểm của nó là sẽ khiến tên biến bị dài hơn thôi.
