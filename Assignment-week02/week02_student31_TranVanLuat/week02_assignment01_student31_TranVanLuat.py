# a)  Những kí tự không được phép xuất hiện trong tên biến trong Python: @ $ %

# b)  + Ví dụ về tên biến hợp lệ:  myNum, age_of_child, number
#     + Ví dụ về tên biến không hợp lệ:   011267, @language, %abc

# c)  Ví dụ: Khi muốn đặt tên một biến, và biến đó dùng để lưu trữ số lượng đơn hàng
#     + Không nên đặt:            a,x,s,..
#     + Nên đặt:                  number_of_goods
#                                 numberOfGoods


# d)  Mô tả các phong các đặt tên biến có trong bài:
#     numberofstudents     lowercase   tên biến chỉ gồm những chữ cái viết thường.
#     NUMBEROFSTUDENTS     UPPERCASE   tên biến chỉ gồm những chữ cái in hoa.
#     numberOFStudents     camelCase   tên biến gồm nhiều từ viết liền nhau, chữ cái đầu tiên của mỗi từ đều viết hoa, trừ từ đầu tiên.                                           
#     NumberOfStudents     PascalCase   tên biến gồm nhiều từ viết liền nhau, chữ cái đầu tiên của mỗi từ đều viết hoa.                                          
#     number_of_students   snake_case  tên biến gồm nhiều từ nối với nhau bởi dấu gạch dưới, các chữ cái đều viết thường.
                                         

# e)  + camelCase:    tên biến gồm một hoặc nhiều từ viết liền nhau,      Ví dụ:  nameStudent
#                     các chữ cái đầu tiên của mỗi từ đều viết hoa,               numberOfStudents        
#                     trừ từ đầu tiên.
#     + PascalCase:   tên biến gồm một hoặc nhiều từ viết liền nhau,      Ví dụ:  StudentManager
#                     các chữ cái đầu tiên của mỗi từ đều viết hoa.
#     + snake_case:   tên biến gồm một hoặc nhiều từ, được nối với        Ví dụ:  name_of_student
#                     nhau bởi dấu gạch dưới, các chữ cái trong mỗi               number_of_students      
#                     từ đều viết thường.

# f)  Bản thân mình thường chọn camelCase hoặc snakecase vì sự rõ ràng, trực quan cũng như thói quen cá nhân