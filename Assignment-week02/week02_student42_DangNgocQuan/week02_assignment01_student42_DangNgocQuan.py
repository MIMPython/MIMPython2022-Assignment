# c)  Ví dụ: Khi muốn đặt tên một biến, và biến đó dùng để lưu trữ số lượng học sinh:
#     + Đặt tên biến không tốt:   x
#                                 s
#     + Đặt tên biến tốt:         number_students
#                                 number_of_students

# d)  Mô tả các phong các đặt tên biến có trong bài:
#     numberofcollegestudents     lowercase   tên biến chỉ gồm những chữ cái viết thường.
#     NUMBEROFCOLLEGESTUDENTS     UPPERCASE   tên biến chỉ gồm những chữ cái in hoa.
#     numberOfCollegeStudents     camelCase   tên biến gồm nhiều từ viết liền nhau, chữ cái 
#                                             đầu tiên của mỗi từ đều viết hoa, trừ từ đầu tiên.
#     NumberOfCollegeStudents     PascalCase   tên biến gồm nhiều từ viết liền nhau, chữ cái 
#                                             đầu tiên của mỗi từ đều viết hoa.
#     number_of_college_students  snake_case  tên biến gồm nhiều từ nối với nhau bởi dấu 
#                                             gạch dưới, các chữ cái đều viết thường.

# e)  + camelCase:    tên biến gồm một hoặc nhiều từ viết liền nhau,      Ví dụ:  nameStudent
#                     các chữ cái đầu tiên của mỗi từ đều viết hoa,               numberOfStudents        
#                     trừ từ đầu tiên.
#     + PascalCase:   tên biến gồm một hoặc nhiều từ viết liền nhau,      Ví dụ:  StudentManager
#                     các chữ cái đầu tiên của mỗi từ đều viết hoa.
#     + snake_case:   tên biến gồm một hoặc nhiều từ, được nối với        Ví dụ:  name_of_student
#                     nhau bởi dấu gạch dưới, các chữ cái trong mỗi               number_of_students      
#                     từ đều viết thường.

# f)  Đối với mình, khi dùng Python, mình sẽ sử dụng phong cách snake_case và camelCase.