
# Cách thực thi câu lệnh do-while trong c++:
#     Chương trình thực thi khối lệnh trong phần do, sau đó kiểm tra điều kiện ở phần while. 
#     Nếu điều kiện xảy ra thì chương trình quay trở lại điểm xuất phát vòng lặp, 
#     nếu điều kiện không xảy ra thì chương trình thoát khỏi vòng lặp.
# Cách thực thi câu lệnh while trong python:
#     Vòng lặp while trong python được dùng để thực hành một khối lệnh nhiều lần cho đến khi điều kiện cho sẵn được thỏa mãn.
#     Và khi điều kiện sai, dòng lệnh ngay sau vòng lặp trong chương trình sẽ lập tức được thực hiện
# Thứ tự xử lí của do-while sẽ là vào -> khối lệnh -> điều kiện
#             còn của  while sẽ là vào -> điều kiện -> khối lệnh
# Khi gọi vòng lặp while, do-while, nếu điều kiện sai,
# vòng lặp while sẽ không được thực hiện lần nào nhưng vòng lặp do-while thực hiện được 1 lần.