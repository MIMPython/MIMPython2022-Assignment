def decrypt(message):
	message += ' '
	decipher = ''
	text = ''
	for letter in message:
		if (letter != ' '):
			i = 0
			text += letter
		else:
			i += 1

			if i == 2 :
				decipher += ' '
			else:
				decipher += list(morseCodeDict.keys())[list(morseCodeDict.values()).index(text)]
				text = ''
                
	return decipher

